    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {                                         
        try {
            int idAluno = Integer.parseInt(jTextField1.getText().trim());
            int idCurso = Integer.parseInt(jTextField2.getText().trim());
            double nota = Double.parseDouble(jTextField3.getText().trim());
            java.sql.Date dataLancada = new java.sql.Date(System.currentTimeMillis());

            try (java.sql.Connection con = database.Database.getConnection()) {
                String sql = "INSERT INTO notas (id_aluno, id_curso, nota, data_lan√ßada) VALUES (?, ?, ?, ?)";
                java.sql.PreparedStatement stmt = con.prepareStatement(sql);
                stmt.setInt(1, idAluno);
                stmt.setInt(2, idCurso);
                stmt.setDouble(3, nota);
                stmt.setDate(4, dataLancada);
                stmt.executeUpdate();
                javax.swing.JOptionPane.showMessageDialog(this, "Nota salva com sucesso!");
            }
        } catch (Exception ex) {
            javax.swing.JOptionPane.showMessageDialog(this, "Erro ao salvar nota: " + ex.getMessage());
        }
    }                                        

